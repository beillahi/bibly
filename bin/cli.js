#!/usr/bin/env node
"use strict";

const meow = require('meow');
const split = require('split');
const getStdin = require('get-stdin');
const bibly = require('../lib/index');

let cli = meow(`
  Usage
    $ bibly < keys

  Options
    --help            Show this message
    --latex SOURCE    Fetch missing references from LaTeX SOURCE file
    --sort            Sort entries by key

  Examples
    $ echo "DBLP:conf/popl/CousotC77" | bibly
    $ bibly --latex main.tex
`, {
  default: {
  }
});

async function fetch(entry) {
  try {
    return await bibly.fetch(entry);
  } catch (e) {
    let message;
    if (e.name === 'StatusCodeError')
      message = e.statusCode;
    else
      message = e;

    console.log(`% unable to fetch entry ${entry}: ${message}`);
  }
}

(async () => {
  let entries;

  if (cli.flags.latex)
    entries = await bibly.missing(cli.flags.latex);

  else if (!process.stdin.isTTY)
    entries = (await getStdin()).split("\n").filter(x => x);

  else
    cli.showHelp();

  console.log(`% generated by ${cli.pkg.name} version ${cli.pkg.version}`);
  console.log();

  if (cli.flags.sort)
    entries = entries.sort();

  for (let record of await Promise.all(entries.map(fetch)))
    if (record)
      console.log(record);

})();
